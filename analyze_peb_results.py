#!/usr/bin/env python3
"""Analyze and visualize PEB group comparison results across tasks.

This script loads the PEB results generated by generate_peb_group_script.py
and produces a summary table showing which DCM parameters differ between
CCD and TDC groups across MDOD, MDOG, MGOD, and MGOG tasks.
"""

from __future__ import annotations

import argparse
from pathlib import Path

import numpy as np
import pandas as pd
from scipy.io import loadmat
import matplotlib.pyplot as plt


TASKS = ("MDOD", "MDOG", "MGOD", "MGOG")
DEFAULT_MODEL = "SENSORY_RELAY__UNC_FLEX"


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Analyze PEB group comparison results.")
    parser.add_argument(
        "--peb-dir",
        type=Path,
        default=Path("group_peb"),
        help="Directory containing PEB .mat files.",
    )
    parser.add_argument(
        "--model",
        default=DEFAULT_MODEL,
        help="DCM model name matching the PEB files.",
    )
    parser.add_argument(
        "--tasks",
        nargs="*",
        default=list(TASKS),
        help="Tasks to analyze.",
    )
    parser.add_argument(
        "--out-dir",
        type=Path,
        default=None,
        help="Output directory for analysis results. Default: peb_dir.",
    )
    return parser.parse_args()


def load_peb_result(peb_file: Path) -> dict | None:
    """Load a single PEB .mat file."""
    if not peb_file.exists():
        print(f"Warning: {peb_file} not found")
        return None

    try:
        mat = loadmat(str(peb_file), squeeze_me=True)
        return mat
    except Exception as e:
        print(f"Error loading {peb_file}: {e}")
        return None


def extract_peb_summary(peb_data: dict, task: str, model: str) -> dict:
    """Extract key PEB statistics from the loaded .mat data."""
    summary = {
        "Task": task,
        "Model": model,
        "Keys": list(peb_data.keys()),
    }

    # Try to access PEB and BMA results
    peb_key = f"PEB_{task}"
    bma_key = f"BMA_{task}"
    m_key = f"M_{task}"
    subjects_key = f"subjects_{task}"
    groups_key = f"groups_{task}"

    # Store subject/group info
    if subjects_key in peb_data:
        subjects = peb_data[subjects_key]
        if isinstance(subjects, np.ndarray):
            subjects = subjects.flatten()
        summary["n_subjects"] = len(subjects) if hasattr(subjects, "__len__") else 1

    if groups_key in peb_data:
        groups = peb_data[groups_key]
        summary["groups"] = groups

    # Extract design matrix info
    if m_key in peb_data:
        m_obj = peb_data[m_key]
        if isinstance(m_obj, dict):
            if "X" in m_obj:
                summary["DesignMatrix"] = m_obj["X"]
            if "Xnames" in m_obj:
                summary["ColumnNames"] = m_obj["Xnames"]

    # Try to access PEB results
    if peb_key in peb_data and peb_data[peb_key].size > 0:
        peb_obj = peb_data[peb_key]

        # Handle cell arrays and structured arrays
        try:
            if isinstance(peb_obj, np.ndarray) and peb_obj.ndim == 0:
                peb_obj = peb_obj.item()

            if isinstance(peb_obj, dict):
                # Extract posterior statistics
                if "Ep" in peb_obj:  # Posterior means
                    ep = peb_obj["Ep"]
                    summary["PosteriorMeans"] = ep.flatten() if hasattr(ep, "flatten") else ep
                if "Cp" in peb_obj:  # Posterior covariance
                    cp = peb_obj["Cp"]
                    summary["PosteriorVar"] = np.diag(cp) if isinstance(cp, np.ndarray) and cp.ndim == 2 else cp
                if "Pp" in peb_obj:  # Prior precision
                    summary["Precision"] = peb_obj["Pp"]
        except Exception as e:
            summary["peb_error"] = str(e)

    # Extract BMA results
    if bma_key in peb_data:
        bma_obj = peb_data[bma_key]
        if isinstance(bma_obj, dict):
            summary["BMA_keys"] = list(bma_obj.keys())
            if "Ep" in bma_obj:
                summary["BMA_Means"] = bma_obj["Ep"]
        elif hasattr(bma_obj, "shape") and bma_obj.size > 0:
            summary["BMA_size"] = bma_obj.shape

    return summary


def print_peb_report(results: dict[str, dict]) -> str:
    """Generate a text report of PEB findings."""
    lines = [
        "=" * 80,
        "PEB GROUP COMPARISON RESULTS",
        "=" * 80,
        "",
        "Question: Which DCM parameters differ between CCD and TDC?",
        "Group coding: TDC = -1, CCD = +1 (column 2 is CCD minus TDC contrast)",
        "",
    ]

    for task, summary in results.items():
        lines.append(f"TASK: {task}")
        lines.append("-" * 80)

        if "file" in summary:
            lines.append(f"Source: {summary['file']}")

        # Show subject/group counts
        if "n_subjects" in summary:
            lines.append(f"N subjects: {summary['n_subjects']}")

        # Show column names (parameter labels)
        if "ColumnNames" in summary:
            col_names = summary["ColumnNames"]
            if isinstance(col_names, np.ndarray):
                col_names = col_names.flatten()
            lines.append(f"Design: {', '.join(str(x) for x in col_names)}")

        # Show posterior means (most important!)
        if "PosteriorMeans" in summary:
            means = summary["PosteriorMeans"]
            if len(means) > 0:
                lines.append("Posterior means (Ep):")
                lines.append(f"  Intercept/Mean: {means[0]:.4f}")
                if len(means) > 1:
                    lines.append(f"  CCD_minus_TDC:  {means[1]:.4f} ← GROUP EFFECT")
                for i in range(2, min(len(means), 6)):
                    lines.append(f"  Param {i}: {means[i]:.4f}")

        # Show posterior variance (confidence)
        if "PosteriorVar" in summary:
            var = summary["PosteriorVar"]
            if len(var) > 1:
                lines.append(f"Posterior std dev (CCD_minus_TDC): {np.sqrt(var[1]):.4f}")

        if "error" in summary:
            lines.append(f"  Error: {summary['error']}")

        lines.append("")

    lines.extend([
        "=" * 80,
        "HOW TO INTERPRET:",
        "  - The 'CCD_minus_TDC' parameter shows group differences in DCM connectivity",
        "  - Posterior mean ≠ 0 with low variance → likely group effect",
        "  - Use SPM's BMA (Bayesian Model Averaging) for model-level inference",
        "  - For detailed review, use spm_dcm_peb_review in MATLAB:",
        f"    >> load('group_peb/PEB_MDOD_{results[list(results.keys())[0]].get('Model', 'SENSORY_RELAY__UNC_FLEX')}.mat')",
        "    >> spm_dcm_peb_review(BMA_MDOD, GCM_MDOD)",
        "=" * 80,
    ])

    return "\n".join(lines)


def create_summary_table(peb_dir: Path, model: str, tasks: list[str]) -> pd.DataFrame:
    """Create a summary table of PEB files and their status."""
    rows = []
    for task in tasks:
        peb_file = peb_dir / f"PEB_{task}_{model}.mat"
        status = "✓" if peb_file.exists() else "✗"
        size_mb = peb_file.stat().st_size / (1024**2) if peb_file.exists() else None
        rows.append({
            "Task": task,
            "Status": status,
            "File": peb_file.name,
            "Size (MB)": f"{size_mb:.1f}" if size_mb else "N/A",
        })

    return pd.DataFrame(rows)


def run() -> None:
    args = parse_args()
    peb_dir = args.peb_dir.resolve()
    out_dir = (args.out_dir or peb_dir).resolve()
    out_dir.mkdir(parents=True, exist_ok=True)

    print(f"\nLoading PEB results from: {peb_dir}")
    print(f"Model: {args.model}")
    print(f"Tasks: {', '.join(args.tasks)}\n")

    # Create file status table
    file_table = create_summary_table(peb_dir, args.model, args.tasks)
    print("PEB Files Generated:")
    print(file_table.to_string(index=False))
    print()

    # Load and process each task
    results = {}
    for task in args.tasks:
        peb_file = peb_dir / f"PEB_{task}_{args.model}.mat"
        print(f"Loading {task}...", end=" ")

        peb_data = load_peb_result(peb_file)
        if peb_data:
            summary = extract_peb_summary(peb_data, task, args.model)
            summary["file"] = str(peb_file)
            results[task] = summary
            print("✓")
        else:
            results[task] = {"file": str(peb_file), "error": "Failed to load"}
            print("✗")

    # Print report
    report = print_peb_report(results)
    print("\n" + report)

    # Save report
    report_file = out_dir / "peb_analysis_report.txt"
    with open(report_file, "w") as f:
        f.write(report)
    print(f"\nReport saved to: {report_file}")

    # Save file table
    table_file = out_dir / "peb_files_summary.csv"
    file_table.to_csv(table_file, index=False)
    print(f"Summary table saved to: {table_file}")

    print("\n" + "=" * 80)
    print("NEXT STEPS:")
    print("  1. Visualize results in MATLAB:")
    print(f"     >> load('{peb_dir}/PEB_MDOD_{args.model}.mat')")
    print(f"     >> spm_dcm_peb_review(BMA_MDOD, GCM_MDOD)")
    print("  2. Or use SPM's PEB results viewer for interactive inspection")
    print("=" * 80)


if __name__ == "__main__":
    run()
